import Foundation

/// The SwiftKeyword type is used when ensuring that properties and parameters don't have the same name as a keyword.
/// The list is not complete.
enum SwiftKeyword: String {
    case `continue`
    case `operator`
    case `self`
    case `private`
    case `public`
    case `throw`
    case `throws`
    case `override`
    case `default`
    case `defer`
}
